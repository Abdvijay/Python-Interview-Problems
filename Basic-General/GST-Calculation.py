def find_total_gst(quantity,price,rate):
    amount = price * quantity
    gst_amount = amount * (rate / 100)
    total_amount = gst_amount + amount
    return gst_amount,total_amount

def gst_invoice(product_name,price,quantity,rate):
    gst_amount,total_amount = find_total_gst(quantity,price,rate)
    print(f'|     {product_name.upper()}           |     {price}             |     {quantity}            |     {rate}              |     {gst_amount}             |     {total_amount:.2f}                 |')
    print('--------------------------------------------------------------------------------------------------------------------------------------------------')

