def find_Total_avg_grade(mark1,mark2,mark3):
    total = mark1 + mark2 + mark3
    avg = total / 3
    if avg > 90: grade = 'O'
    elif avg > 80: grade = 'A+'
    elif avg > 70: grade = 'A'
    elif avg > 60: grade = 'B+'
    elif avg > 50: grade = 'B'
    elif avg > 40: grade = 'C'
    elif avg > 35: grade = 'E'
    else: grade = 'F'
    return total,avg,grade

def student_report_card(name,mark1,mark2,mark3):
    total,avg,grade = find_Total_avg_grade(mark1,mark2,mark3)
    print(f'|     {name.upper()}     |     {mark1}         |     {mark2}         |     {mark3}         |     {total}       |     {avg:.2f}       |     {grade}        |')
    print('---------------------------------------------------------------------------------------------------------------------')

